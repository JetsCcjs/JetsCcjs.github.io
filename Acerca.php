<h1 class="ConsejoEstiramiento">Consejos de EStiramientos</h1>
<p class="DescripcionConsejodeEstiramiento">
- Antes de realizar estiramientos adecuados,
es importante estirar el cuerpo con ejercicios
adecuados y a su vez se debe fortalecer los
musculos del cuerpo.
<br>
En este caso, debemos realizar estiramientos suaves,
en donde nuestro cuerpo pueda ejercer adecuadamente
el paso de su funcionamiento de la circulacion.	
</p>
</div>


<div class="col-sm-4" id="MantenerCadaEstiramiento">
<h1>	
Mantener la posicion para su estiramiento
</h1>
<p class="DescripciondeMantenerlaPostura">
Para obtener los beneficios de un estiramiento, es recomendable mantener cada posición durante al menos 15 a 30 segundos. Esto permite que el músculo se relaje y se alargue gradualmente.
</p>
</div>



 <div class="col-sm-4" id="RespirarProfundamenteDiv">
<h1>Respira Profundamente</h1>

<p>La respiración profunda y constante ayuda a relajar los músculos y a aumentar el oxígeno disponible para los tejidos.Para eso, Inhala profundamente antes de iniciar el estiramiento y exhala mientras mantienes la posición.</p>
</div>


<div class="col-sm-4" id="EstiramientoMuscular">
 <h1>
Estiramiento muscular
 </h1>
<p>
Antes de estirar todos los grupos musculares principales, incluyendo cuello, hombros, espalda, caderas, piernas y tobillos. Esto ayuda a mantener un equilibrio muscular y a prevenir desbalances que pueden llevar a lesiones.
</p>
</div>
